#15. When will the else part of try-except-else be executed?


#In a try-except-else block in Python, the else part will be executed if no exceptions are raised within the try block. 
#This means that if the code inside the try block executes without any exceptions being raised, the code in the else block will run. 
#If an exception occurs within the try block, the else block will be skipped.
#Try:This block will test the excepted error to occur.
#Except:If any exception occurs,the try cluses will be skipped and except clause will run.
#Else:If there is no exception then this block will be executed.